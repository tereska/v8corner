function test() {
  /* Unikam inlinowania funkcji. **********************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     **************************************************
     --------------------------------------------------
  */
  var sum = 0;
  for (var i = 0; i < 1000; i++) {
    sum++;
    i++;
    if (i >= 1000) break;
    sum++;
  }
  return sum;
}

function measure() {
  var f = test;
  var start = Date.now();
  for (var i = 0; i < 1000000; i++) {
    f();
  }
  return (Date.now() - start);
}

